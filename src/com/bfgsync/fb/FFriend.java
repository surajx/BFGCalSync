package com.bfgsync.fb;

public class FFriend
{
	private String name;
	private String fID;
	private String Bday;
	
	/**
	 * 
	 * @return
	 */
	public String getName(){
		return name;
	}
	/**
	 * 
	 * @param name
	 */
	public void setName(String name){
		this.name=name;	
	}
	
	/**
	 * 
	 * @return
	 */
	public String getFID(){
		return fID;		
	}
	/**
	 * 
	 * @param fID
	 */
	public void setFID(String fID){
		this.fID=fID;
	}
	
	/**
	 * 
	 * @return
	 */
	public String getBday(){
		return Bday;
	
	}
	/**
	 * 
	 * @param Bday
	 */
	public void setBday(String Bday){
		this.Bday=Bday;
	}
}
